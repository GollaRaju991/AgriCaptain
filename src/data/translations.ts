// Helper to translate a product name
export function translateProductName(name: string, lang: string): string {
  if (lang === 'en') return name;
  
  // Try to match the base product name (before the " - X" suffix)
  const parts = name.split(' - ');
  const baseName = parts[0];
  const suffix = parts.length > 1 ? ` - ${parts.slice(1).join(' - ')}` : '';
  
  const translated = productNameTranslations[baseName]?.[lang];
  if (translated) return translated + suffix;
  
  return name;
}

// Helper to translate a product description
export function translateDescription(desc: string, lang: string): string {
  if (lang === 'en') return desc;
  return descriptionTranslations[desc]?.[lang] || desc;
}
